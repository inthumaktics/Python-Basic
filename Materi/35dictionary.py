# DICTIONARY 
# list -->> array, mengakses dengan menggunakan index

data_list = ["Erika", "ellinor", "rariru"]
print(data_list[0])

# dictionary (dict) -->> associative array
# identifier -->> key 

data_dict = {
    'key': 'value',
    'er':'Erika',
    'ell': 'ellinor',
    'rr':'rariru',
    'number': 10.0000,
    'list' : data_list,
}

print(data_dict['rr'])
